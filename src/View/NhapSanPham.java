    @Override
    public <T> void showData(List<T> list, DefaultTableModel model) {
        model.setRowCount(0);
        int n = 1;
        for (T t : list) {
            if (t instanceof Sach) {
                Sach s = (Sach) t;
                model.addRow(new Object[]{
                    n++, s.getMaSach(), s.getTenSach(), s.getTheLoai(), s.getTacGia(),
                    NumberUtils.formatCurrency(s.getGia())
                });
            }
        }
    }
