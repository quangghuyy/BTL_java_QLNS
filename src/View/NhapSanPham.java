    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {

            String tenSach = txtTenSach.getText().trim();
            String theLoai = txtTheLoai.getText().trim();
            String tacGia = txtTacGia.getText().trim();
            String giaText = txtGia.getText().trim();

            NhapSachCtrl.validateNotEmpty(tenSach, theLoai, tacGia, giaText);

            NhapSachCtrl.validateGia(giaText);

            NhapSachCtrl.validateTenSachDuyNhat(tenSach, NhapSachCtrl.getList());

            double gia = Double.parseDouble(giaText);
            String maSach = AutoIdGenerator.generateMaSach();
            Sach s = new Sach(maSach, tenSach, theLoai, tacGia, gia);
            addSach(s);
            JOptionPane.showMessageDialog(null, "Sách đã được thêm thành công.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Giá trị của giá không hợp lệ. Vui lòng nhập lại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblSach.getSelectedRow();
      
        if (selectedRow >= 0) {
            try {
                Sach selectedSach = NhapSachCtrl.getList().get(selectedRow);
                SuaSachForm suaForm = new SuaSachForm(this, (JFrame) javax.swing.SwingUtilities.getWindowAncestor(this),
                        true, selectedSach);
                suaForm.setVisible(true);

                showData(NhapSachCtrl.getList(), modelSach);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một sách để sửa.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

