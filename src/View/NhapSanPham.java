    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSach.getSelectedRow();
        try {
            if (selectedRow == -1) {
                throw new Exception("Vui lòng chọn một sách để xóa.");
            }
            int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa sách có mã là "
                    + NhapSachCtrl.getList().get(selectedRow).getMaSach() + " ?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                NhapSachCtrl.getList().remove(selectedRow);
                showData(NhapSachCtrl.getList(), modelSach);
                NhapSachCtrl.updateFile();
                clearTextFields();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

