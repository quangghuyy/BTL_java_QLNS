    public void ListSelectionListener() {
        tblSach.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting() && tblSach.getSelectedRow() != -1) {
                    int selectedRow = tblSach.getSelectedRow();
                    Sach selectedSach = NhapSachCtrl.getList().get(selectedRow);

                    txtTenSach.setText(selectedSach.getTenSach());
                    txtTheLoai.setText(selectedSach.getTheLoai());
                    txtTacGia.setText(selectedSach.getTacGia());
                    txtGia.setText(selectedSach.getFormattedGia());
                }
            }
        });
    }

