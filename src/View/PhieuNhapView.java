    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedRow = tblPhieuNhap.getSelectedRow();
        try {
            if (selectedRow == -1) {
                throw new Exception("Vui lòng chọn một phiếu để xóa.");
            }
            int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa phiếu có mã là "
                    + NhapHangCtrl.getList().get(selectedRow).getMaPhieu() + " ?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                NhapHangCtrl.getList().remove(selectedRow);
                showData(NhapHangCtrl.getList(), modelPhieu);
                NhapHangCtrl.updateFile();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int selectedRow = tblPhieuNhap.getSelectedRow();
        try {
            if (selectedRow == -1) {
                throw new Exception("Vui lòng chọn một phiếu để xóa.");
            }
            PhieuNhap selectedPhieu = NhapHangCtrl.getList().get(selectedRow);
            SuaPhieuNhap spn = new SuaPhieuNhap(this, (JFrame) javax.swing.SwingUtilities.getWindowAncestor(this),
                    true, (Phieu) selectedPhieu);
            spn.setVisible(true);
            showData(NhapHangCtrl.getList(), modelPhieu);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_btnSuaActionPerformed

