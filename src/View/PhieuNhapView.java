    @Override
    public <T> void showData(List<T> list, DefaultTableModel model) {
        model.setRowCount(0);
        int n = 1;
        for (T t : list) {
            if (t instanceof PhieuNhap) {
                PhieuNhap pN = (PhieuNhap) t;
                model.addRow(new Object[]{
                    n++, pN.getMaPhieu(), pN.getNhaCungCap(), pN.getThoiGianTao(),
                    NumberUtils.formatCurrency(pN.getTongThanhToan())
                });
            }
        }
    }
